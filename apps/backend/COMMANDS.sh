#!/usr/bin/env bash

# This script helps you quickly generate NestJS components using the CLI
# Run these commands from the apps/backend directory

# Generate a new module
# nest generate module <name>
# Example:
# nest generate module users

# Generate a controller
# nest generate controller <name>
# Example:
# nest generate controller users

# Generate a service
# nest generate service <name>
# Example:
# nest generate service users

# Generate a complete feature (module + controller + service)
# nest generate resource <name>
# Example:
# nest generate resource users

# Common NestJS CLI commands:
# nest new <app>              - Create a new NestJS project
# nest generate module <name> - Generate a module
# nest generate controller <name> - Generate a controller
# nest generate service <name> - Generate a service
# nest generate gateway <name> - Generate a WebSocket gateway
# nest generate decorator <name> - Generate a custom decorator
# nest build                   - Build the application
# nest start                   - Run the application
# nest info                    - Display CLI environment information

# For detailed documentation on NestJS CLI:
# https://docs.nestjs.com/cli/overview
